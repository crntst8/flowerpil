const ReleaseActionRow = ({ actions = [] }) => {
  if (!actions || actions.length === 0) {
    return null;
  }

  return (
    <ActionRowContainer>
      {actions.map((action, index) => {
        const platform = PLATFORM_ICONS[action.platform_key] || PLATFORM_ICONS.custom;
        const iconKey = action.platform_key === 'website' || action.platform_key === 'custom' ? 'link' : action.platform_key;
        const Icon = PlatformIcons[iconKey] || PlatformIcons.link;

        return (
          <ActionLink
            key={action.id || index}
            href={action.url}
            target="_blank"
            rel="noopener noreferrer"
            $color={platform.color}
            title={action.label || platform.name}
          >
            {Icon}
          </ActionLink>
        );
      })}
    </ActionRowContainer>
  );
};

